package com.mrlou.data;

public class FangWenData {
	private String url;
	private String prams;
	public FangWenData(String url, String prams) {
		super();
		this.url = url;
		this.prams = prams;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getPrams() {
		return prams;
	}
	public void setPrams(String prams) {
		this.prams = prams;
	}
	
	
	

}
